package com.xiaoh.springboot.config.bean;

import org.springframework.stereotype.Component;

/**
 * @author xiaoH
 * @date 2019/10/16
 * @description
 */
//@Component
public class Dog {

    private String name;
    private Integer age;

    @Override
    public String toString() {
        return "Dog{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }
}
